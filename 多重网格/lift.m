function E=lift(e,m)
    M=2*(m+1)-1;
    E=zeros(M+2,M+2);
    E(2:2:M+1,1:2:M+2)=1/2*e(1:m+1,1:m+2)+1/2*e(2:m+2,1:m+2);%竖直
    E(1:2:M+2,2:2:M+1)=1/2*e(1:m+2,1:m+1)+1/2*e(1:m+2,2:m+2);%水平
    E(2:2:M+1,2:2:M+1)=1/2*e(2:m+2,1:m+1)+1/2*e(1:m+1,2:m+2);%斜
    E(1:2:M+2,1:2:M+2)=e(1:m+2,1:m+2);
end